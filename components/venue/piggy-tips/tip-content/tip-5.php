  <p class="tips-info">You can’t come to Amsterdam and not experience the culture. Just around the corner from the Downtown hostel, you’ll find cultural hotspots such as Dam square, the Royal Palace and the Anne Frank museum. For a little dose of history, the Anne Frank House is somewhere we definitely recommend and it’s only 14 minutes walk from the hostel - it’s one of Amsterdam’s top sites for a reason and it will really give you an insight into her life in hiding. The cobblestoned Dam Square is located in the historical center of Amsterdam and is the centre-point for the National Monument and Madame Tussauds.
    <br><br>
  The Sex Museum is literally just across the road from the hostel (2 mins walk) where you can gain insight into Amsterdam’s more seductive culture.</p>
