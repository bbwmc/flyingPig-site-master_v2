            <h4>
              Car
            </h4>
            <p>
              We don’t recommend driving in Amsterdam. Parking is very expensive, public transport can take you everywhere and pretty much everything is walking distance.
            </p>
            <p>
              If you are traveling by car, it’s better to park a little outside the center. The best and cheapest spot to park your car is the Olympic Stadium Parking (Olympisch Stadion 44). Ask for the special P + R rate of € 8, - (per 24h), which also includes 2 free tram tickets. Tram 16 takes you back to the Museumplein and Central Station.
