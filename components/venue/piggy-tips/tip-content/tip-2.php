    <h4>
      Dutch Language (some handy stuff):
    </h4>

    <dl class="language-tips">
      <dt>
        Yes / No
      </dt>
      <dd>
        - Jah / Nay (Ja / Nee)
      </dd>
      <dt>
        Thanks
      </dt>
      <dd>
        Buhdanked (Bedankt)
      </dd>
      <dt>
        Hello
      </dt>
      <dd>
        - Hoy (Hoi)
      </dd>
      <dt>
        Bye
      </dt>
      <dd>
        - Duoy (Doei)
      </dd>
      <dt>
        I want a beer
      </dt>
      <dd>
        - Dew mar un Biertjuh (Doe maar een biertje.)
      </dd>
      <dt>
        How much is it
      </dt>
      <dd>
        - Huvail cost daht? (Hoe veel kost dat?)
      </dd>
      <dt>
        What’s your name?
      </dt>
      <dd>
        - Hu heit juh? (Hoe heet je?)
      </dd>
    </dl>
    <dl class="language-tips">
      <dt>
        Do you have a lighter?
      </dt>
      <dd>
        - Heb juh un fuhrtjuh? (Heb je een vuurtje?)
      </dd>
      <dt>
        Want to smoke some?
      </dt>
      <dd>
        - Trackje? (Trekje?)
      </dd>
      <dt>
        Please piss off
      </dt>
      <dd>
       - Flicker op (Flikker op!) - Perhaps change it to “Rot op!”
      </dd>
      <dt>
        I need condoms
      </dt>
      <dd>
        - Eek heb kaputjuhs nowdik (Ik heb een kapotje nodig.)
      </dd>
      <dt>
         I need directions
      </dt>
      <dd>
        - Ik heb richtingen nodig
      </dd>
      <dt>
